#!/bin/sh
###############################################################################################
###############################################################################################
for i in "$@"
do
old_name="$i"
new_name=${old_name%.*}_unbracketed.krn
sed -E -e 's:^(\*MM[0-9]+)\t\*\t\*:\1\t\1\t\1:g' \
    -Ee 's/\{([^{}]+)\}/\1/g' \
    -Ee 's/\}\*\*//g' ${i} > $new_name 
done

exit
